let Fruit_Chaat = ["Banana", "Apple", "Melon"]

console.log(Fruit_Chaat);
// push() method
// push addition in the list

Fruit_Chaat.push("Orange");

console.log(Fruit_Chaat);

//Pop() Methode
// pop methode remove the last one in the list

Fruit_Chaat.pop();

console.log(Fruit_Chaat);

// Concat Methode
// concat methode adding a array in list / adding more the one things

Fruit_Chaat = Fruit_Chaat.concat(["Guava", "Strawberry", "Sapodilla Plum"]);
console.log(Fruit_Chaat);
